package com.day02;

/*
 * 논리 연산자
 * &&, ||, !(부정)
 * */

public class oprEx04 {
    public static void main(String[] args) {
        boolean a = true, b = false;
        System.out.println("a && b : " + (a && b));
        System.out.println("a || b : " + (a || b));
        System.out.println("    !a : " + (!a));


    }
}
